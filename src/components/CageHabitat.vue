		<h1>A Chinchilla's Cage</h1>
		<p>A chinchilla’s cage should have solid floors and ramps. The larger the cage, the better. The recommended chinchilla cage has a minimum floor space of about 24 by 24 inches, and the taller the cage, the better. A tall chinchilla cage with shelves and ladders allow the chinchilla to climb is best for activity. Wire is the best cage material as it cannot be chewed easily.</p>

		<h2>Cage Bedding</h2>
		<p>Many chinchilla cages have a wire floor, which is nice for cleanliness but can be hard on chinchilla's feet. These are best avoided. Wire floors or shelves can be covered with wood or plastic to avoid injuries. The tray can be lined with wood shavings (avoid cedar shavings, and hardwood shavings such as aspen are preferred over pine), recycled paper such as Care Fresh brand, or newspapers. Bedding should be changed at least once a week.</p>

		<h2>Chinchilla Habitats</h2>
		<p>Chinchilla hideouts can include plastic houses, wooden houses, or hay houses. Chinchillas enjoy the coziness of the houses, and it’s not uncommon for two or three all at once to be hanging out in one. Houses will have to be replaced every few months, sometimes sooner, sometimes longer depending on the material of their house and activity of your chinchilla.</p>

		<h3>Cleaning a Chinchilla Cage</h3>
		<p>Cleaning a chinchilla cage can be a hassle, but can be made simple with a few tips.</p>
		<ul>
			<li>Vacuum the shelves of the cage with a small vacuum every other day to remove excess droppings from your cage. This will make your initial cleaning day easier.</li>
			<li>Litter box train your chinchilla. Though you cannot control where they leave their droppings, you can teach them to urinate in a litter pan. This will help reduce smell and dirtiness of the cage.</li>
